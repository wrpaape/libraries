inline size_t utf8_header_size(char *bytes)
{
	if (((*bytes) & 0x80) == 0x00)
		return 1ul;	/* ASCII character*/

	if (((*bytes) & 0x40) == 0x00)
		return 0ul;	/* not unicode */

	if (((*bytes) & 0x20) == 0x00)
		return 2ul;	/* 110xxxxx */

	if (((*bytes) & 0x10) == 0x00)
		return 3ul;	/* 1110xxxx */
}


inline bool is_utf8(char *bytes)
{
	return utf8_size(bytes) > 0ul;
}

