utf8_t inline decode(utf8_t* state, utf8_t* codep, utf8_t byte) {

  utf8_t type = utf8d[byte];

  *codep = (*state != UTF8_ACCEPT) ?
    (byte & 0x3fu) | (*codep << 6) :
    (0xff >> type) & (byte);

  *state = utf8d[256 + *state*16 + type];

  return *state;
}

inline bool is_utf8_string(char *string)
{
	while (1) {
		if (*string == '\0')
			return true;

		if (utf8_size(*string))
			++string;
		else
			return false;
	}
}

